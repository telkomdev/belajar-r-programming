
# Relational Operator (Operator relasi) mengembalikan nilai BOOLEAN (TRUE/FALSE)

# Operator lebih dari >
gt <- 10 > 5
print(gt)

# Operator kurang dari <
lt <- 10 < 5
print(lt)

# Operator sama dengan ==
eq <- 10 == 10
print(eq)

# Operator lebih dari atau sama dengan >=
gte <- 10 >= 5
print(gte)

# Operator kurang dari atau sama dengan <=
lte <- 4 <= 5
print(lte)

# Operator tidak sama dengan !=
ne <- 3 != 4
print(ne)