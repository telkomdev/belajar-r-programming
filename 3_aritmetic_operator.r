# Arithmetic operator (Operator Aritmatika)

# Addtition + (Penambahan)
a <- 5 + 2
print(a)

# Substract - (Pembagian)
s <- 5 - 2
print(s)

# Multiply - (Perkalian)
m <- 5 * 2
print(m)

# Divide / (Pembagian)
d <- 10 / 2
print(d)

# Exponential ^ (Pemangkatan)
e <- 5 ^ 2
print(e)

# Modulus(sisa dari hasil pembagian) %%
r <- 10 %% 2
print(1)

# Integer Division %/% (Pembagian tanpa embel-embel koma)
q <- 10 %/% 8
print(q)



