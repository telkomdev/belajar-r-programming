
# Character Data Type

# nama variabel = name
# value = "Tony stark"
name <- "Tony stark"
print(name)
print(class(name))

char_a <- 'a';
print(char_a)
print(class(char_a))

# Logical Data Type
# Logical hanya mempunyai dua nilai, yaitu TRUE dan FALSE

# nama variabel = yes
# value = TRUE
yes <- TRUE
print(yes)
print(class(yes))

# nama variabel = no
# value = FALSE
no <- FALSE
print(no)
print(class(no))

a <- 10 > 5
print(a)

# Numeric Data Type
# Numeric biasanya digunakan untuk menyimpan value angka decimal seperti currency dan lain-lain.

myMoney <- 300.50
print(myMoney)
print(class(myMoney))

# Integer Data Type

# nama variabel = age
# value = 30
age <- 30L
print(age)
print(class(age))

# Complex Data Type

# nama variabel = v
# value = 1 + 2i
v <- 1+2i
print(v)
print(sqrt(v))
print(class(v))
